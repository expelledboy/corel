%%------------------------------------------------------------------------------

-module(corel_lists).

%% API
-export([
         remove_duplicates/1,
         randomize/1
        ]).

%%------------------------------------------------------------------------------
%% API
%%------------------------------------------------------------------------------

%% @doc remove duplicate terms in list preserving order
remove_duplicates([])    -> [];
remove_duplicates([H|T]) -> [H | [X || X <- remove_duplicates(T), X /= H]].

%%------------------------------------------------------------------------------

randomize(List) when is_list(List) ->
  [X||{_,X} <- lists:sort([ {random:uniform(), N} || N <- List])].

