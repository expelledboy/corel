%%------------------------------------------------------------------------------

-module(corel_io).

%% API
-export([
         format/2,
         print/2
        ]).

%%------------------------------------------------------------------------------
%% API
%%------------------------------------------------------------------------------

format(Format,Params) ->
  lists:flatten(io_lib:format(Format, Params)).

print(Format,Params) ->
  io:format(Format,Params).

